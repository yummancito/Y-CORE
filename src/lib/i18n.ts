const dicts: Record<Language, Dict> = { es, en, fr, pt, de, zh, hi }

let currentLang: Language = 'es'

export function detectSystemLanguage(locale: string): Language {
  const lang = locale.split('-')[0].toLowerCase()
  if (lang in dicts) return lang as Language
  return 'en'
}

export function setLanguage(lang: string) {
  if (lang in dicts) currentLang = lang as Language
}

export function t(key: string): string {
  return dicts[currentLang]?.[key] || dicts.en[key] || key
}
